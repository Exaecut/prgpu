#[cfg(target_os = "windows")]
use cudarc::nvrtc::{CompileError, CompileOptions};

use crate::cpu::codegen::{generate_cpu_dispatch_wrapper, parse_kernel_signature};
use crate::gpu::shaders::expand_includes_runtime;

pub mod sdk;
pub mod compile;
pub mod reflection;
pub mod bindings;
pub mod cpu_dispatch;

type DynError = Box<dyn Error + Send + Sync>;

#[cfg(target_os = "windows")]
pub fn parse_nvrtc_error(err: &CompileError) -> String {
	match err {
		CompileError::CompileError { log, .. } => {
			let log = log.to_string_lossy();

			let mut out = String::new();
			let mut current_block = Vec::new();

			for line in log.lines() {
				let line = line.trim_end();

				if line.contains("note #") {
					continue;
				}

				if line.contains("): error:") && !current_block.is_empty() {
					out.push_str(&format_block(&current_block));
					current_block.clear();
				}

				current_block.push(line.to_string());
			}

			if !current_block.is_empty() {
				out.push_str(&format_block(&current_block));
			}

			if out.is_empty() { log.into_owned() } else { out }
		}

		other => format!("{:#?}", other),
	}
}

fn format_block(block: &[String]) -> String {
	let mut out = String::new();

	if let Some(header) = block.first()
		&& let Some((path_part, rest)) = header.split_once("): ")
		&& let Some((path, line)) = path_part.rsplit_once('(')
	{
		let file = path.split('\\').next_back().unwrap_or(path);

		out.push_str(&format!("\nerror: {}\n", rest));
		out.push_str(&format!(" --> {}:{}\n", file, line));
		out.push_str("  |\n");

		for l in &block[1..] {
			out.push_str(&format!("  {}\n", l));
		}

		return out;
	}

	for l in block {
		out.push_str(l);
		out.push('\n');
	}

	out
}

pub fn compile_shaders(shader_dir: &str) -> Result<(), DynError> {
	let out_dir = std::env::var("OUT_DIR").unwrap();
	let out_path = PathBuf::from(&out_dir);

	let shader_dir_abs = PathBuf::from(shader_dir).canonicalize().unwrap();

	// Compile .slang files via slangc (Slang pipeline)
	compile_slang_shaders(&shader_dir_abs, &out_path)?;

	// Compile .vekl files via existing pipeline
	compile_vekl_shaders(&shader_dir_abs, &out_path)?;

	Ok(())
}

fn compile_slang_shaders(shader_dir: &PathBuf, out_dir: &PathBuf) -> Result<(), DynError> {
	let slang_files: Vec<PathBuf> = std::fs::read_dir(shader_dir)
		.unwrap()
		.filter_map(|e| e.ok())
		.map(|e| e.path())
		.filter(|p| p.extension().and_then(|s| s.to_str()) == Some("slang"))
		.collect();

	if slang_files.is_empty() {
		return Ok(());
	}

	let sdk = sdk::sdk_dir();
	let slangc = sdk::slangc_bin(&sdk);
	if !slangc.exists() {
		panic!(
			"slangc not found at {}. Slang SDK v{} auto-download failed.\n\
			 Manually download from: https://github.com/shader-slang/slang/releases/tag/{}",
			slangc.display(), sdk::SLANG_VERSION, sdk::SLANG_TAG
		);
	}

	let vekl_dir = PathBuf::from(env!("CARGO_MANIFEST_DIR")).parent().unwrap().join("vekl");
	let include_dirs = vec![shader_dir.clone(), vekl_dir];

	for slang_file in &slang_files {
		let name = slang_file.file_stem().unwrap().to_str().unwrap().to_string();

		let compiled = compile::compile_shader(
			&sdk, slang_file, &name, out_dir, &include_dirs,
		);

		cpu_dispatch::compile_cpu_cpp(&compiled.cpp_path, &sdk);

		let mut all_bindings = String::from("// Auto-generated by prgpu build from slangc -reflection-json\n\n");

		if let Some(metal_ref_path) = &compiled.metal_reflection_path {
			let json = std::fs::read_to_string(metal_ref_path)?;
			let refl = reflection::parse_reflection(&json)
				.unwrap_or_else(|e| panic!("Failed to parse Metal reflection JSON: {e}"));
			all_bindings.push_str("// --- Metal target bindings ---\n");
			all_bindings.push_str(&bindings::generate_bindings(&refl, &format!("METAL_{name}")));
			all_bindings.push('\n');
		}

		if let Some(cuda_ref_path) = &compiled.cuda_reflection_path {
			let json = std::fs::read_to_string(cuda_ref_path)?;
			let refl = reflection::parse_reflection(&json)
				.unwrap_or_else(|e| panic!("Failed to parse CUDA reflection JSON: {e}"));
			all_bindings.push_str("// --- CUDA target bindings ---\n");
			all_bindings.push_str(&bindings::generate_bindings(&refl, &format!("CUDA_{name}")));
			all_bindings.push('\n');
		}

		let cpu_json = std::fs::read_to_string(&compiled.cpu_reflection_path)?;
		let cpu_refl = reflection::parse_reflection(&cpu_json)
			.unwrap_or_else(|e| panic!("Failed to parse CPU reflection JSON: {e}"));
		all_bindings.push_str("// --- CPU target bindings ---\n");
		all_bindings.push_str(&bindings::generate_bindings(&cpu_refl, "CPU"));

		let bindings_path = out_dir.join(format!("{name}_bindings.rs"));
		std::fs::write(&bindings_path, &all_bindings)?;
		println!("cargo:warning=[slang] Binding map written to: {}", bindings_path.display());
	}

	Ok(())
}

fn compile_vekl_shaders(shader_dir: &PathBuf, out_dir: &PathBuf) -> Result<(), DynError> {
	let utils = PathBuf::from(env!("CARGO_MANIFEST_DIR")).parent().unwrap().join("vekl").canonicalize().unwrap();
	let utils_str = utils.to_string_lossy().replace("\\\\?\\", "");
	let include_dirs = vec![utils.clone()];

	#[cfg(target_os = "windows")]
	let cuda_include = {
		let cuda_path = std::env::var("CUDA_HOME").or_else(|_| std::env::var("CUDA_PATH")).unwrap_or("/usr/local/cuda".into());
		PathBuf::from(&cuda_path).join("include").canonicalize().unwrap()
	};

	let mut cpu_sources: Vec<(String, PathBuf)> = Vec::new();

	for entry in std::fs::read_dir(shader_dir).unwrap() {
		let path = entry.unwrap().path();

		if path.extension().and_then(|s| s.to_str()) != Some("vekl") {
			continue;
		}

		let name = path.file_stem().unwrap().to_str().unwrap().to_string();
		let src = std::fs::read_to_string(&path).unwrap();
		let expanded_metal = expand_includes_runtime(&src, shader_dir, &include_dirs).map_err(|e| format!("Failed to flatten Metal source for {}: {e}", path.display()))?;
		let metal_path = out_dir.join(format!("{name}.metal"));
		std::fs::write(&metal_path, expanded_metal)?;
		println!("cargo:warning=Metal shader source generated -> {}", metal_path.to_str().unwrap());

		for (suffix, half_precision) in [("", false), ("_f16", true)] {
			let tag = format!("{name}{suffix}");
			let ptx_path = out_dir.join(format!("{tag}.ptx"));

			#[cfg(target_os = "windows")]
			{
				use crate::gpu::shaders::prepare_cuda_source;
				let prepared_src = prepare_cuda_source(&src, &name);
				let mut extra_opts = vec![
					"--std=c++14".into(),
					"--extra-device-vectorization".into(),
					"--device-as-default-execution-space".into(),
					"-DVEKL_CUDA=1".into(),
				];
				if cfg!(debug_assertions) {
					extra_opts.push("-DDEBUG=1".into());
				}

				if half_precision {
					extra_opts.push("-DUSE_HALF_PRECISION=1".into());
				}

				let opts = CompileOptions {
					ftz: Some(true),
					prec_sqrt: Some(false),
					prec_div: Some(false),
					fmad: Some(true),
					use_fast_math: None,
					include_paths: vec![utils_str.clone(), cuda_include.to_string_lossy().replace("\\\\?\\", "")],
					arch: Some("compute_86"),
					options: extra_opts,
					..Default::default()
				};

				let ptx = cudarc::nvrtc::compile_ptx_with_opts(&prepared_src, opts).map_err(|e| {
					let pretty = parse_nvrtc_error(&e);
					eprintln!("Compile failed [{tag}]:\n{pretty}");
					println!("cargo:warning=Compile failed [{tag}]:\n{pretty}");
					Box::new(e) as DynError
				})?;

				let ptx_bytes = ptx.as_bytes().unwrap();
				let ptx_bytes = if ptx_bytes.last() == Some(&0) {
					&ptx_bytes[..ptx_bytes.len() - 1]
				} else {
					ptx_bytes
				};
				std::fs::write(&ptx_path, ptx_bytes)?;
				println!("cargo:warning=Shader compiled successfully to -> {}", ptx_path.to_str().unwrap());
			}

			#[cfg(not(target_os = "windows"))]
			{
				let _ = half_precision;
				std::fs::write(&ptx_path, b"")?;
				println!("cargo:warning=CUDA PTX placeholder generated -> {}", ptx_path.to_str().unwrap());
			}
		}

		let sig = parse_kernel_signature(&src).ok_or_else(|| format!("Failed to parse kernel signature in {}", path.display()))?;

		let shader_abs = path.canonicalize().unwrap();
		let shader_abs_str = shader_abs.to_string_lossy().replace("\\\\?\\", "").replace("//?/", "").replace("\\", "/");
		let wrapper_code = generate_cpu_dispatch_wrapper(&shader_abs_str, &sig);

		let wrapper_path = out_dir.join(format!("{}_cpu_dispatch.cpp", name));
		std::fs::write(&wrapper_path, &wrapper_code)?;

		println!("cargo:warning=CPU dispatch wrapper generated -> {}", wrapper_path.to_str().unwrap());

		cpu_sources.push((name, wrapper_path));
	}

	if !cpu_sources.is_empty() {
		let cpu_debug = cfg!(debug_assertions)
			|| std::env::var("EX_CPU_DEBUG").unwrap_or_default() == "1";
		println!("cargo:rerun-if-env-changed=EX_CPU_DEBUG");

		let mut build = cc::Build::new();
		build
			.cpp(true)
			.include(&utils_str)
			.include(shader_dir.to_str().unwrap())
			.define("VEKL_CPU", Some("1"))
			.flag_if_supported("/Zc:preprocessor")
			.flag_if_supported("/std:c++14")
			.flag_if_supported("-std=c++14")
			.flag_if_supported("/Z7")
			.flag_if_supported("-g");

		if cpu_debug {
			build.opt_level(0);
			build.define("DEBUG", Some("1"));
			println!("cargo:warning=CPU kernels compiled in DEBUG mode (EX_CPU_DEBUG or debug_assertions)");
		} else {
			build
				.opt_level(3)
				.flag_if_supported("/fp:fast")
				.flag_if_supported("-ffast-math")
				.flag_if_supported("/Oi")
				.flag_if_supported("/arch:AVX2")
				.flag_if_supported("-mavx2")
				.flag_if_supported("-mfma");
		}

		for (name, wrapper_path) in &cpu_sources {
			build.file(wrapper_path);
			println!("cargo:warning=Compiling CPU kernel: {} (debug={})", name, cpu_debug);
		}

		let pkg_name = std::env::var("CARGO_PKG_NAME").unwrap_or("unknown".into());
		let lib_name = format!("{}_cpu_kernels", pkg_name);
		build.compile(&lib_name);
		println!("cargo:warning=CPU shader library compiled: {}", lib_name);
	}

	Ok(())
}
